package com.Labwork;

public class DoubleToFloat {

	public static void main(String[] args) {
		
		char ch='a';
		int x=(char)ch;
		
		System.out.println(x);
		
		float f=23122.54f;
		int in=(int)f;
		
		System.out.println(in);
		
		int ii=876;
		float ff=ii;  // int to float is possible because there is not any loss of data
		
		System.out.println(ff);

	}

}
