package com.basics;

public class TypeCasting {
	public static void main(String args[])
	{
		byte num1=110;
		byte num2=20;
		 byte sum=(byte)(num1+num2);
		 System.out.println(sum);
		 short s1=78;
		 short s2=56;
		 
		 short r= (short)(s1+s2);
		 
		 System.out.println(r);
		 
		 System.out.println(sum);
		 
		 float n1=(float)78.35;
		 float n2=67.3f;
		 
		 int a=90;
		 int b=45;
		 
		 float ans=a+b;
		 
		 System.out.println(ans);
		 
		 
	}

}
